export function getCityBySlug(slug: string): typeof cities[0] | undefined {
  return cities.find((c) => c.slug === slug);
}

export function getCitiesByCategory(category: string): typeof cities[0][] {
  if (category === 'all') return cities;
  return cities.filter((c) => c.categories.includes(category));
}

export function getFeaturedCities(): typeof cities[0][] {
  return cities.slice(0, 6);
}

export function getAllCitySlugs(): string[] {
  return cities.map((c) => c.slug);
}

