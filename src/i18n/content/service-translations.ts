/**
 * Service content translations.
 */
import type { Locale } from '../types';
import type { Service, ServiceFAQ } from '@/types';
import { services as baseServices, getServiceBySlug as baseGetBySlug } from '@/data/services';

export interface ServiceTranslationOverlay {
  title?: string;
  subtitle?: string;
  category?: string;
  location?: string;
  durationLabel?: string;
  description?: string;
  longDescription?: string;
  whatIsIncluded?: string[];
  highlights?: string[];
  suitableFor?: string[];
  notSuitableFor?: string[];
  process?: { title: string; description: string }[];
  faq?: ServiceFAQ[];
  tiers?: Record<string, { name: string; label: string; price?: string; includes: string[] }>;
  seoKeywords?: string[];
}

type TranslationsMap = Record<string, ServiceTranslationOverlay>;
type LocaleTranslations = Partial<Record<Locale, TranslationsMap>>;
const allTranslations: LocaleTranslations = {};

export function setServiceTranslations(locale: Locale, data: TranslationsMap): void {
  allTranslations[locale] = data;
}

function applyTranslation(svc: Service, t: ServiceTranslationOverlay | undefined): Service {
  if (!t) return svc;
  const sl = svc.slug;
  return {
    ...svc,
    title: t.title ?? svc.title,
    subtitle: t.subtitle ?? svc.subtitle,
    category: t.category ?? svc.category,
    location: t.location ?? svc.location,
    durationLabel: t.durationLabel ?? svc.durationLabel,
    description: t.description ?? svc.description,
    longDescription: t.longDescription ?? svc.longDescription,
    whatIsIncluded: t.whatIsIncluded ?? svc.whatIsIncluded,
    highlights: t.highlights ?? svc.highlights,
    suitableFor: t.suitableFor ?? svc.suitableFor,
    notSuitableFor: t.notSuitableFor ?? svc.notSuitableFor,
    process: t.process ? t.process.map((p, i) => ({ step: i + 1, ...p })) : svc.process,
    faq: t.faq ?? svc.faq,
    tiers: svc.tiers.map((tier, i) => {
      const tt = t.tiers?.[tier.slug];
      if (!tt) return tier;
      return { ...tier, name: tt.name, label: tt.label, includes: tt.includes };
    }) as Service['tiers'],
    seoKeywords: t.seoKeywords ?? svc.seoKeywords,
  };
}

export function getServices(locale: Locale): Service[] {
  const overlay = allTranslations[locale];
  if (!overlay) return baseServices;
  return baseServices.map((s) => applyTranslation(s, overlay[s.slug]));
}

export function getServiceBySlug(slug: string, locale: Locale): Service | undefined {
  const svc = baseGetBySlug(slug);
  if (!svc) return undefined;
  const overlay = allTranslations[locale];
  return applyTranslation(svc, overlay?.[slug]);
}

export function getServicesByIndustry(industry: string, locale: Locale): Service[] {
  return getServices(locale).filter((s) => s.industry === industry);
}

